using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;
using Microsoft.AspNetCore.Mvc;
using Microsoft.AspNetCore.Mvc.RazorPages;
using Microsoft.EntityFrameworkCore;
using AnimationCharacters.Models;
using AnimationCharacters.Data;

namespace AnimationCharacters.Pages_Character
{
    public class DetailsModel : PageModel
    {
        private readonly AnimationCharactersContext _context;

        public DetailsModel(AnimationCharactersContext context)
        {
            _context = context;
        }

        public Character Character { get; set; }

        public async Task<IActionResult> OnGetAsync(int? id)
        {
            if (id == null)
            {
                return NotFound();
            }

            Character = await _context.Character.FirstOrDefaultAsync(m => m.ID == id);

            if (Character == null)
            {
                return NotFound();
            }
            return Page();
        }
    }
}
